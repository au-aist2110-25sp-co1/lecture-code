def doo_doo(prefix):
    print(f'{prefix}, doo-doo-doo-doo-doo-doo')

def shark(prefix):
    doo_doo(prefix)
    doo_doo(prefix)
    doo_doo(prefix)
    print(f'{prefix}!')
    print()

shark('Baby shark')
shark('Mommy shark')
shark('Daddy shark')
shark('Grandma shark')
shark('Grandpa shark')
shark('Let\'s go hunt')
shark('Run away')
shark('Safe at last')


