# Your favorite color is green.
# Ask the user for their favorite color.
# If the color is green then say that's your favorite too.
# Otherwise say their color is ugly.

color = input("What is your favorite color? ")

if color == "green":
    print("Oh boy!! My favorite color is green too!!!! You rock!")
else:
    print("My favorite color is green. I think " + color + " is really ugly!!!")

